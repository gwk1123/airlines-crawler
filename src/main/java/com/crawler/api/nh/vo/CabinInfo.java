package com.crawler.api.nh.vo;

public class CabinInfo {

    /**
     * 舱位
     */
    private String name;

    /**
     * 舱位数
     */
    private String info;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }
}
